# 2.复合赋值运算符：+=  -=  *=  /=  //=  %=  **=
m1 = 3
m1 = 9    # 重新赋值
print(m1)

n1 = 3
n1 += 9  # 等价于n1 = n1 + 9,先计算n1 + 9,然后给n1进行重新赋值
print(n1)

# 问题1：一定要书写成+=，而不是+ =
a = 3
a *= 10
print(a)

