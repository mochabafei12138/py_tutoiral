# 1.简单赋值运算符：=
name = 'abc'    # 定义变量，将‘abc’赋值给name
name = 'xyz'    # 重新赋值，将'xyz'赋值给name

# =的右边只要是一个数据或者可以计算结果的表达式，都可以给=左边的变量进行赋值
n1 = True
print(n1)
n2 = 2 + 3 * 2 - 10 + 2 ** 10
print(n2)  # 1022

